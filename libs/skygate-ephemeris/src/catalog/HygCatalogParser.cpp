constexpr std::size_t kHygProgressCallbackInterval = 512;
constexpr std::size_t kMinHygRowCountLimit = 2000000;
constexpr std::size_t kMinPlausibleHygRowBytes = 6;

std::optional<double> parseFiniteDouble(const QStringView text)
{
    const QString trimmed = text.trimmed().toString();
    if (trimmed.isEmpty()) {
        return std::nullopt;
    }

    bool isDouble = false;
    const double value = trimmed.toDouble(&isDouble);
    if (!isDouble || !std::isfinite(value)) {
        return std::nullopt;
    }

    return value;
}

std::string toStdString(const QString& text)
{
    const QByteArray utf8 = text.toUtf8();
    return std::string(utf8.constData(), static_cast<std::size_t>(utf8.size()));
}

